    CORALOGIX_RUM_SCREENSHOT_THRESHOLD: $CORALOGIX_RUM_SCREENSHOT_THRESHOLD,
    CORALOGIX_RUM_SCREENSHOT_COOLDOWN_MS: $CORALOGIX_RUM_SCREENSHOT_COOLDOWN_MS,
    CORALOGIX_RUM_SCREENSHOT_MAX_PER_SESSION: $CORALOGIX_RUM_SCREENSHOT_MAX_PER_SESSION,
    CORALOGIX_RUM_SCREENSHOT_MAX_ERRORS: $CORALOGIX_RUM_SCREENSHOT_MAX_ERRORS,
    CORALOGIX_RUM_SESSION_RECORDING_SAMPLE_RATE: $CORALOGIX_RUM_SESSION_RECORDING_SAMPLE_RATE,
    CORALOGIX_OTLP_TRACES_URL: $CORALOGIX_OTLP_TRACES_URL
  }' >> "$target"
